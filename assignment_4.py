"""**6. Remove duplicate characters**"""

def remove_duplicates(text):
  seen = set()
  unique_text = ""
  for char in text:
    if char not in seen:
      seen.add(char)
      unique_text += char
  return unique_text

text = input("Enter a string: ")
unique_text = remove_duplicates(text)
print("String with duplicates removed:", unique_text)

